public class YourChild {
    String yourChildStr, childNameStr, teacherClassStr, schoolStr, presentTodayStr;

    public String getChildName() {
        return childNameStr;
    }

    public void setChildName(String childNameStr) {
        this.childNameStr = childNameStr;
    }

    public String getTeacherClass() {
        return teacherClassStr;
    }

    public void setTeacherClass(String teacherClassStr) {
        this.teacherClassStr = teacherClassStr;
    }

    public String getSchool() {
        return schoolStr;
    }

    public void setSchool(String schoolStr) {
        this.schoolStr = schoolStr;
    }

    public String getPresentToday() {
        return presentTodayStr;
    }

    public void setPresentToday(String presentTodayStr) {
        this.presentTodayStr = presentTodayStr;
    }

    public String getYourChild() {
        return yourChildStr;
    }

    public void setYourChildStr(String yourChildStr) {
        this.yourChildStr = yourChildStr;
    }
}
